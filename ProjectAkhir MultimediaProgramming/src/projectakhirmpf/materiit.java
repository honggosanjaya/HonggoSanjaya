        int num1;
        num1 = Integer.parseInt(this.loop.getText());
        DefaultListModel model;
        model = new DefaultListModel();
        
        int i=1;
        while(i<num1)
        {
            model.addElement(i);
            i++;
        }
        lis.setModel(model);
      
