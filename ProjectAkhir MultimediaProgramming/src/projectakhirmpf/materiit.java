    private void execActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_execActionPerformed
        // TODO add your handling code here:
        int number;
        
        try{
            number = Integer.parseInt(this.score.getText());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Wrong Data", "error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String skor = null;
        String kata = null;
        if(number>=90)
        {
            skor = "A";
            kata = "Congrats !";
        }
        else if ((number>=80) && (number<90))
        {
            skor = "B";
            kata = "Good !";
        }
        else if ((number>=70) && (number<80))
        {
            skor = "C";
            kata = "Better !";
        }
        else if ((number>=60) && (number<70))
        {
            skor = "D";
            kata = "More Improvemnet Please !";
        }
        else
        {
            skor = "E";
            kata = "Get a Urgent Help !";
        }
        
        this.result.setText("Your Score is : "+skor+", "+kata);
    }//GEN-LAST:event_execActionPerformed

